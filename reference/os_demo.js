const os = require('os')

// Platform
console.log(os.platform());

// CPU ARCH
console.log(os.arch());

// CPU Core Info
console.log(os.cpus());

// Free Memory
console.log(os.freemem());

// total memory
console.log(os.totalmem());

// Home dir
console.log(os.homedir());

// uptime 
console.log(os.uptime());